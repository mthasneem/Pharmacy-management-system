        public ArrayList<drugs_data> getDrugsList(){
    ArrayList<drugs_data> drugsList = new ArrayList<drugs_data>();
    Connection Conn = main.getConnection();
    String sql = "Select * from drugs";
    Statement stmt;
    ResultSet rs;
    
    try{
        stmt = Conn.createStatement();
        rs = stmt.executeQuery(sql);
        drugs_data drugs;
        
        while (rs.next()){
            drugs = new drugs_data(rs.getString("drug_name"),rs.getString("drug_formula"),rs.getString("drug_mfd"),rs.getString("drug_exp"));
            drugsList.add(drugs);
        }
    } catch(SQLException e){
        Logger.getLogger(edit_delete_drugs.class.getName()).log(Level.SEVERE,null,e);
    }return drugsList;
}
public void showDrugsFromTable(){
    ArrayList<drugs_data> list = getDrugsList();
    DefaultTableModel model = (DefaultTableModel) drugtable.getModel();
    model.setRowCount(0);
    Object[]row = new Object[4];
    for (int x=0; x< list.size();x++){
        row[0] = list.get(x).getdname();
        row[1] = list.get(x).getdformula();
        row[2] = list.get(x).getdmfd();
        row[3] = list.get(x).getdexp();
        model.addRow(row);
        
    }
    
}

