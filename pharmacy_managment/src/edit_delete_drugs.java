    private void drug_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_drug_updateActionPerformed
        // TODO add your handling code here:
        try{
            String UpdateQuery=null;
            PreparedStatement ps = null;
            Connection Conn = main.getConnection();
            
            UpdateQuery = "UPDATE drugs SET drug_name = ? ,drug_formula = ?, drug_mfd = ? , drug_exp = ?";
            ps = (PreparedStatement) Conn.prepareCall(UpdateQuery);
            
            ps.setString (1, drug_name01.getText());
            ps.setString (2,drug_formula01.getText());
            ps.setString(3, drug_mfd01.getText());
            ps.setString (4, drug_exp01.getText());
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog (null, "Employee Details Updated!");
            showDrugsFromTable();
        }catch (SQLException e) {
            Logger.getLogger (edit_delete_drugs.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_drug_updateActionPerformed

    private void drug_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_drug_deleteActionPerformed
        // TODO add your handling code here:
        String DrugName = drug_name01.getText();
        try {
            if(DrugName.equals("")){
                JOptionPane.showMessageDialog(null, "No DRUG is Selected");
            } else {
                Connection conn = main.getConnection();
                Statement stmt = conn.createStatement();
                
                String sql ="DELETE FROM `drugs` WHERE `drug_name` ='"+DrugName+"'";
                stmt.executeUpdate(sql);
                String msg = "Drug was deleted frome pharmacy";
                JOptionPane. showMessageDialog (null, msg);
                showDrugsFromTable();

            }
        }catch (SQLException e) {
            String retry = "Plesse Try Again(!)";
            JOptionPane.showMessageDialog(null, retry);
        }
    }//GEN-LAST:event_drug_deleteActionPerformed

